package modeloBeans;


public class BeansVeiculo {
    private int cod;
    private String marca_veiculo;
    private String placa_veiculo;
    private String ano_veiculo;
    private String chassi;
    private String veiculopesquisa;

    public int getCod() {
        return cod;
    }

    public void setCod(int cod) {
        this.cod = cod;
    }

    public String getMarca_veiculo() {
        return marca_veiculo;
    }

    public void setMarca_veiculo(String marca_veiculo) {
        this.marca_veiculo = marca_veiculo;
    }

    public String getPlaca_veiculo() {
        return placa_veiculo;
    }

    public void setPlaca_veiculo(String placa_veiculo) {
        this.placa_veiculo = placa_veiculo;
    }


    public String getAno_veiculo() {
        return ano_veiculo;
    }

    public void setAno_veiculo(String ano_veiculo) {
        this.ano_veiculo = ano_veiculo;
    }

    public String getChassi() {
        return chassi;
    }

    public void setChassi(String chassi) {
        this.chassi = chassi;
    }

    public String getveiculopesquisa() {
        return veiculopesquisa;
    }

    public void setveiculopesquisa(String vpesquisa) {
        this.veiculopesquisa = vpesquisa;
    }


}
//PABLO
$(document).ready(function () {
    $(function () {
        fetch('https://apigsw.herokuapp.com/data/horascolaborador/pablomartins/')
            .then(function (task) {
                return task.json();
            })
            .then(function (dados) {
                $("colab475").append(dados[1]),
                    $("colab476").append(dados[2]);
                $("colab477").append(dados[4]);
                $("colab478").append(dados[5]);
            });
    });
});

